class Product {
  final int? id;
  final String? name;
  final double? price;
  final double? priceAfterDiscount;
  final int? quantity;
  final String? note;
  final DateTime? createdAt;

  Product({
    this.id,
    this.name,
    this.price,
    this.priceAfterDiscount,
    this.quantity,
    this.note,
    this.createdAt,
  });
}
